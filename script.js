function handleAddProduct(e) {
    e.preventDefault();

    const name = document.getElementById('productName').value;
    const price = parseFloat(document.getElementById('productPrice').value);
    const category = document.getElementById('productCategory').value;
    const description = document.getElementById('productDescription').value;
    const originalPrice = parseFloat(document.getElementById('productOriginalPrice').value) || null;
    
    // Get image source (URL or file)
    const imageSource = document.querySelector('input[name="imageSource"]:checked').value;
    let imageUrl = '';
    
    if (imageSource === 'url') {
        imageUrl = document.getElementById('productImage').value;
    } else {
        // Handle file upload - convert to data URL
        const file = document.getElementById('productImageFile').files[0];
        if (file) {
            imageUrl = URL.createObjectURL(file);
        }
    }

    const categoryIcons = {
        'Kitchen': 'fa-blender',
        'Home': 'fa-house',
        'Cleaning': 'fa-broom',
        'Lighting': 'fa-lightbulb',
        'Cooling': 'fa-fan',
        'Electronics': 'fa-plug',
        'Furniture': 'fa-couch',
        'Other': 'fa-box'
    };

    const newProduct = {
        id: products.length + 1,
        name,
        price,
        originalPrice,
        category,
        icon: categoryIcons[category] || 'fa-box',
        image: imageUrl || 'https://images.unsplash.com/photo-1523275335684-37898b6baf30?w=400&h=400&fit=crop',
        description
    };

    products.unshift(newProduct);
    renderProducts(products);
    closeAddProductModal();

    anime({
        targets: '.product-card:first-child',
        scale: [0.5, 1],
        rotate: [(-10, 10), 0],
        duration: 600,
        easing: 'easeOutBack'
    });
}

// ========================================
// EVENT LISTENERS
// ========================================
function setupEventListeners() {
    // Search
    searchBtn.addEventListener('click', () => {
        searchProducts(searchInput.value);
    });

    searchInput.addEventListener('keyup', (e) => {
        if (e.key === 'Enter') {
            searchProducts(searchInput.value);
        } else {
            searchProducts(searchInput.value);
        }
    });

    // Checkout
    checkoutBtn.addEventListener('click', goToCheckout);
    backToShopBtn.addEventListener('click', backToShop);
    placeOrderBtn.addEventListener('click', placeOrder);
    successCloseBtn.addEventListener('click', closeSuccessMessage);

    // Promo code
    applyPromoBtn.addEventListener('click', applyPromoCode);
    promoCodeInput.addEventListener('keyup', (e) => {
        if (e.key === 'Enter') {
            applyPromoCode();
        }
    });

    // Product Detail Modal
    detailClose.addEventListener('click', closeProductDetailModal);
    productDetailModal.addEventListener('click', (e) => {
        if (e.target === productDetailModal) closeProductDetailModal();
    });
    detailAddToCart.addEventListener('click', () => {
        const productId = parseInt(detailAddToCart.dataset.id);
        addToCart(productId);
    });

    // Authentication
    signInBtn.addEventListener('click', openSignInModal);
    signUpBtn.addEventListener('click', openSignUpModal);
    logOutBtn.addEventListener('click', handleLogOut);
    signInForm.addEventListener('submit', handleSignIn);
    signUpForm.addEventListener('submit', handleSignUp);
    signInClose.addEventListener('click', closeSignInModal);
    signUpClose.addEventListener('click', closeSignUpModal);
    signInCancel.addEventListener('click', closeSignInModal);
    signUpCancel.addEventListener('click', closeSignUpModal);
    
    signInModal.addEventListener('click', (e) => {
        if (e.target === signInModal) closeSignInModal();
    });
    signUpModal.addEventListener('click', (e) => {
        if (e.target === signUpModal) closeSignUpModal();
    });

    // Add Product Modal
    sellBtn.addEventListener('click', openAddProductModal);
    modalClose.addEventListener('click', closeAddProductModal);
    cancelBtn.addEventListener('click', closeAddProductModal);
    modalOverlay.addEventListener('click', (e) => {
        if (e.target === modalOverlay) {
            closeAddProductModal();
        }
    });
    addProductForm.addEventListener('submit', handleAddProduct);
    
    // Image source toggle
    document.querySelectorAll('input[name="imageSource"]').forEach(radio => {
        radio.addEventListener('change', (e) => {
            if (e.target.value === 'url') {
                imageUrlInput.style.display = 'block';
                imageFileInput.style.display = 'none';
            } else {
                imageUrlInput.style.display = 'none';
                imageFileInput.style.display = 'block';
            }
        });
    });

    // Keyboard shortcuts
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape') {
            closeAddProductModal();
            closeSignInModal();
            closeSignUpModal();
            closeSuccessMessage();
            closeProductDetailModal();
        }
    });
}


// ========================================
// PROMO CODE FUNCTIONALITY
// ========================================
function applyPromoCode() {
    const code = promoCodeInput.value.trim().toUpperCase();
    
    if (!code) {
        promoMessage.textContent = 'Please enter a promo code';
        promoMessage.className = 'promo-message error';
        return;
    }
    
    if (promoCodes.hasOwnProperty(code)) {
        appliedPromo = code;
        const discountPercent = promoCodes[code] * 100;
        
        const subtotal = cart.reduce((sum, item) => sum + (item.price * item.quantity), 0);
        const discount = subtotal * promoCodes[code];
        const newTotal = subtotal - discount;
        
        discountAmount.textContent = `- RM ${discount.toFixed(2)}`;
        discountRow.style.display = 'flex';
        
        summaryTotal.textContent = `Total: RM ${newTotal.toFixed(2)}`;
        
        promoMessage.textContent = `Promo code applied! You get ${discountPercent}% off!`;
        promoMessage.className = 'promo-message success';
        
        anime({
            targets: '.promo-section',
            scale: [1, 1.02, 1],
            duration: 300,
            easing: 'easeInOutQuad'
        });
    } else {
        promoMessage.textContent = 'Invalid promo code. Try WELCOME for 10% off!';
        promoMessage.className = 'promo-message error';
        appliedPromo = null;
        discountRow.style.display = 'none';
        
        const subtotal = cart.reduce((sum, item) => sum + (item.price * item.quantity), 0);
        summaryTotal.textContent = `Total: RM ${subtotal.toFixed(2)}`;
    }
}


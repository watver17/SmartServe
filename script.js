// ========================================
// RENDER PRODUCTS
// ========================================
function renderProducts(productsToRender) {
    if (productsToRender.length === 0) {
        productGrid.innerHTML = `
            <div class="empty-state">
                <i class="fas fa-search"></i>
                <h3>No products found</h3>
                <p>Try a different search term</p>
            </div>
        `;
        return;
    }

    productGrid.innerHTML = productsToRender.map((product, index) => `
        <div class="product-card" data-id="${product.id}" style="opacity: 0;">
            <div class="product-image" onclick="openProductDetail(${product.id})">
                <img src="${product.image}" alt="${product.name}" onerror="this.style.display='none'; this.nextElementSibling.style.display='flex';">
                <i class="fas ${product.icon}" style="display: none;"></i>
                <span class="category-tag">${product.category}</span>
            </div>
            <div class="product-info" onclick="openProductDetail(${product.id})">
                <h3>${product.name}</h3>
                <div class="price">
                    RM ${product.price.toFixed(2)}
                    ${product.originalPrice ? `<span class="original-price">RM ${product.originalPrice.toFixed(2)}</span>` : ''}
                </div>
            </div>
            <button class="add-to-cart-btn" data-id="${product.id}">
                <i class="fas fa-cart-plus"></i> Add to Cart
            </button>
        </div>
    `).join('');

    anime({
        targets: '.product-card',
        translateY: [20, 0],
        opacity: [0, 1],
        duration: 600,
        delay: anime.stagger(100),
        easing: 'easeOutQuad'
    });

    document.querySelectorAll('.add-to-cart-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.stopPropagation();
            const productId = parseInt(btn.dataset.id);
            addToCart(productId);
        });
    });
}

